import java.util.Scanner;
public class example32_08 {

	public static void main(String[] args) {
		
		Scanner in = new Scanner(System.in);
		System.out.println("Программа проверяет число делится ли оно на 4 и не меньше ли 10"); //описание
		System.out.println("Введите число для проверки:"); //запрос
		try {
		int number = in.nextInt(); //ввод числа
		if ((number % 4)==0 && number>=10) { //проверка при делении на 4 and не меньше 10
			System.out.println("Ура!Число делится на 4 и оно не меньше 10.");
				}
		else {System.out.println("Увы! Условие Не выполняется");
		       }
		}
		catch (Exception error) {System.out.println("При работе программы произошла ошибка!"); //обработка исключения
		System.out.println("Возможно вы некорректно ввели данные!");}
		in.close();
	}

}
